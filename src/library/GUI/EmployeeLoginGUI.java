    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:

        String id = jTextFieldID.getText();
        String pass = jTextFieldPassword.getText();

        UserDAO userDAO = new UserDAO();
        try {
            boolean emp = userDAO.findEmployee(id,pass);
            
            if (jTextFieldID.getText().equals("admin") && jTextFieldPassword.getText().equals("admin")) {
                BookGUI book = new BookGUI();

                book.setVisible(true);

                this.setVisible(false);
            } else if (emp == true) {
                OrderGUI book = new OrderGUI(id);

                book.setVisible(true);

                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "WORNG ID OR PASSWORD!!!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeLoginGUI.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButtonLoginActionPerformed

